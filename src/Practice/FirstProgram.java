package Practice;

public class FirstProgram {

	public static void main(String[] args) {
		System.out.println("this is first Programs");
		
		System.out.println("\"kishor wadkar\"");   // print statement in between "  "
		System.out.println("\'kishor wadkar\'");  // print statement in between '  '
		System.out.println("\bkishor wadkar\b"); // print statement in between ? ?
		System.out.println("\tkishor wadkar\t");  // leave space starting 
		System.out.println("\nkishor wadkar\n");  // leave space for single line
		System.out.println("\rkishor wadkar\r"); //leave Space for two line
		System.out.println("\\kishor wadkar\\");  // print statement in between \ \

	}

}
